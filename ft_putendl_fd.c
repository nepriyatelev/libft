#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	unsigned int	i;

	i = 0;
	if (fd >= 0 && s)
	{	
		while (s[i] != '\0')
			write(fd, &s[i++], 1);
	}
	write(fd, "\n", 1);
}
